void my_MPI_Bcast(double *data, int length, MPI_Comm comm)
{
    int myid, np;
    MPI_Comm_rank(comm, &myid);
    MPI_Comm_size(comm, &np);
    //printf("myid %d  np  %d\n", myid, np);
    MPI_Barrier(comm);
    printf("myid %d  np  %d\n", myid, np);
    int i, j;

    if (myid == 0)
    {
        for (i = 1; i < np; i = i * 2)
        {
            //        printf("MPI_Send myid %d i %d  np  %d\n", myid, i, np);

            MPI_Send(data, length, MPI_DOUBLE, i, i, comm);

        }

    }
    else
    {
        MPI_Status status;
        i = 1;
        while (i * 2 <= myid)
            i = i * 2;


        MPI_Recv(data, length, MPI_DOUBLE,  myid - i, myid, comm, &status);
        //  printf("MPI_Recv myid %d from %d np  %d\n", myid, myid - i, np);

        i = i * 2;
        while (myid + i < np)
        {
            //    printf("MPI_Send myid %d to i %d  np  %d\n", myid, myid + i, np);

            MPI_Send(data, length, MPI_DOUBLE, myid + i, myid + i, comm);
            i = i * 2;

        }

    }
    // printf("myid %d  np  %d\n", myid, np);


}



